"""
Базовые классы для паттерна Observer
"""
from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Optional, Any, Dict
from datetime import datetime


@dataclass
class FileEvent:
    """Объект переноса данных (DTO) для репрезентации файлового события в системе.

    Используется для инкапсуляции метаданных о действиях с файлами и их передачи
    между издателями событий и компонентами-наблюдателями (аудит, логирование).
    Класс представляет собой унифицированную структуру данных, не привязанную
    напрямую к состоянию ORM-моделей в базе данных.

    Attributes:
        event_type (str): Строковой маркер операции. Допустимые значения:
            ``'upload'``, ``'download'``, ``'rename'``, ``'move'``, ``'delete'``, ``'update'``.
        file_id (int, optional): Идентификатор целевого файла. Может отсутствовать
            для незарегистрированных или удаленных ресурсов.
        file_name (str): Наименование файла на момент фиксации события.
        user_id (int, optional): Первичный ключ пользователя, совершившего действие.
        user_email (str, optional): Электронная почта пользователя для быстрой
            идентификации в текстовых логах.
        timestamp (datetime): Астрономическое время и дата регистрации события.
        old_value (str, optional): Предыдущее состояние изменяемого параметра
            (например, старое имя при переименовании или старый путь при перемещении).
        new_value (str, optional): Целевое состояние изменяемого параметра
            (например, новое имя или целевой путь директории).
        details (dict): Дополнительный структурированный контекст события в формате JSON.
        ip_address (str, optional): Сетевой IPv4/IPv6 адрес клиента, инициировавшего запрос.

    Methods:
        __post_init__(): Финализирует инициализацию полей объекта по умолчанию.
    """
    event_type: str  # upload, download, rename, move, delete, update
    file_id: Optional[int]
    file_name: str
    user_id: Optional[int]
    user_email: Optional[str]
    timestamp: datetime
    old_value: Optional[str] = None
    new_value: Optional[str] = None
    details: Dict[str, Any] = None
    ip_address: Optional[str] = None
    
    def __post_init__(self):
        """Выполняет пост-инициализацию объекта события после сборки конструктора.

                Гарантирует, что словарь дополнительных деталей ``details`` всегда будет
                инициализирован (заменен на пустой словарь при значении ``None``), а также
                проставляет текущую метку времени ``datetime.now()``, если аргумент
                ``timestamp`` не был передан явно.
        """
        if self.details is None:
            self.details = {}
        if self.timestamp is None:
            self.timestamp = datetime.now()


class FileObserver(ABC):
    """Абстрактный базовый класс для реализации наблюдателей за файловыми событиями.

    Определяет унифицированный интерфейс для всех подписчиков (слушателей) в рамках
    паттерна «Наблюдатель» (Observer). Любой компонент, которому необходимо реагировать
    на операции с файлами (например, подсистемы аудита, квотирования, индексации или
    уведомлений), должен наследоваться от данного класса и реализовывать его интерфейс.
    """
    
    @abstractmethod
    def update(self, event: FileEvent) -> None:
        """Абстрактный метод обработки входящего файлового события.

        Вызывается издателем (:class:`FileSubject`) для каждого зарегистрированного
        подписчика при фиксации новой файловой активности. Каждая конкретная
        реализация должна содержать собственную логику реагирования на переданное событие.

        Args:
            event (FileEvent): Объект файлового события, содержащий полные
                метаданные выполненной операции.

        Returns:
            None: Метод предназначен для выполнения побочных эффектов
            (запись в БД, отправка уведомлений, инвалидация кеша).
        """
        pass


class FileSubject:
    """Базовый класс издателя (Subject) в паттерне проектирования «Наблюдатель».

    Обеспечивает управление жизненным циклом подписчиков и координирует процесс
    рассылки уведомлений. Любой компонент системы, генерирующий файловые события
    и требующий реакции со стороны других подсистем, должен наследоваться от данного
    класса или использовать его интерфейс.

    Attributes:
        _observers (list[FileObserver]): Внутренний список зарегистрированных
            наблюдателей, ожидающих уведомления о событиях.

    Methods:
        attach(observer): Добавляет нового наблюдателя в список рассылки.
        detach(observer): Удаляет существующего наблюдателя из списка рассылки.
        notify(event): Оповещает всех зарегистрированных наблюдателей о событии.
    """
    
    def __init__(self):
        """Инициализирует пустой список для хранения зарегистрированных наблюдателей."""
        self._observers: list[FileObserver] = []
    
    def attach(self, observer: FileObserver) -> None:
        """Регистрирует (подписывает) нового наблюдателя на получение событий.

        Проверяет отсутствие переданного экземпляра в текущем пуле подписчиков
        перед добавлением, чтобы предотвратить дублирование уведомлений.

        Args:
            observer (FileObserver): Экземпляр класса наблюдателя, реализующий
                интерфейс ``FileObserver``.
        """
        if observer not in self._observers:
            self._observers.append(observer)
    
    def detach(self, observer: FileObserver) -> None:
        """Удаляет (отписывает) наблюдателя из списка рассылки событий.

        Исключает экземпляр из пула подписчиков, если он был зарегистрирован ранее,
        прекращая отправку последующих уведомлений файловой активности.

        Args:
            observer (FileObserver): Ранее зарегистрированный экземпляр наблюдателя.
        """
        if observer in self._observers:
            self._observers.remove(observer)
    
    def notify(self, event: FileEvent) -> None:
        """Последовательно уведомляет всех зарегистрированных наблюдателей о новом событии.

        Итерируется по внутреннему списку подписчиков и вызывает у каждого метод
        ``update()``. Безопасный цикл изолирует выполнение каждого наблюдателя:
        ошибки внутри одного подписчика логируются, но не блокируют доставку
        события остальным слушателям.

        Args:
            event (FileEvent): Объект файлового события, содержащий метаданные
                выполненного действия для передачи подписчикам.

        Note:
            Исключения, возникающие при обработке события наблюдателями, перехватываются
            и записываются в системный лог через ``logger.error``, защищая стабильность
            процесса диспетчеризации.
        """
        for observer in self._observers:
            try:
                observer.update(event)
            except Exception as e:
                import logging
                logger = logging.getLogger(__name__)
                logger.error(f"Error notifying observer {observer.__class__.__name__}: {str(e)}")
